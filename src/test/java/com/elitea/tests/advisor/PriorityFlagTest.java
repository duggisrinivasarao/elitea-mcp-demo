    @Test
    @Order(1)
    @DisplayName("AC1: Flag account for priority review returns 200 with flagged=true")
    void test_flag_client_account_returns_200_with_valid_payload() {
        Map<String, Object> requestBody = new HashMap<>();
        requestBody.put("flagged", true);
        requestBody.put("reason", "PORTFOLIO_CONCERN");
        requestBody.put("reasonNote", "Client portfolio shows significant risk exposure.");

        given()
            .contentType(ContentType.JSON)
            .header("Authorization", "Bearer " + AUTH_TOKEN)
            .body(requestBody)
        .when()
            .put("/clients/" + CLIENT_ID + "/flag")
        .then()
            .statusCode(200)
            .body("clientId", equalTo(CLIENT_ID))
            .body("flagged", equalTo(true))
            .body("reason", equalTo("PORTFOLIO_CONCERN"))
            .body("flaggedAt", notNullValue())
            .body("flaggedBy", notNullValue());
    }

