    @Order(4)
    @DisplayName("AC2: Reason note visible in flagged accounts dashboard summary")
    void test_reason_note_visible_in_dashboard_account_summary() {
        Response response = given()
            .contentType(ContentType.JSON)
            .header("Authorization", "Bearer " + AUTH_TOKEN)
        .when()
            .get("/advisor/dashboard/flagged")
        .then()
            .statusCode(200)
            .body("$", not(empty()))
            .body("collect { it.clientId }", hasItem(CLIENT_ID))
            .extract().response();

        String reasonNote = response.jsonPath().getString(
            "find { it.clientId == '" + CLIENT_ID + "' }.reasonNote"
        );
        assertNotNull(reasonNote, "reasonNote must be visible in the account summary entry");
    }

